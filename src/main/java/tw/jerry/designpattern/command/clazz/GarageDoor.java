package tw.jerry.designpattern.command.clazz;

public class GarageDoor {

	public void up() {
		System.out.println("Door is up");
	}
	public void down() {
		System.out.println("Door is down");
	}
	public void pause() {
		System.out.println("Door is pause");
	}
	public void lightOn() {
		System.out.println("Door is lightOn");
	}
	public void lightOff() {
		System.out.println("Door is lightOff");
	}
	
}
